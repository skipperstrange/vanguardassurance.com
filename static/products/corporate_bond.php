
    <strong>Bonds are usually a contract between three parties – the Contractor, Guarantor and the Principal.</strong>
    with the guarantor providing the guarantee that the principal who engages the services of any Contractor will be indemnified in the event of default in delivering of services as expected. At Vanguard Assurance, we serve as Guarantors to provide all kinds of Bonds to enable you transact your business in peace and tranquility. Some of these Bonds include:

<div class="">
    <h4 class="">General Bonds</h4>
    <ol>
        <li>
            <strong>Bid Bond/Bid Security/Tender Security:</strong>
        This Bond is taken by contractors who want to tender for projects. Vanguard guarantees to pay the principal for amounts up to the bond value, in the event that, either the Contractor or Bidder withdraws from the bidding process, or the Contractor fails to sign the contract or take possession of the site to commence work. You will require this bond mostly to tender for a contract.</li>
        <li>
            <strong>Performance Bond:</strong>
        This policy obliges the Guarantor (Vanguard) to ensure that the Contractor will perform the job awarded to completion and to the satisfaction of the principal (i.e. according to specifications and within the given time), or else arrange to complete the work. Failure by the Contractor to perform as expected, the guarantor will be called upon to pay the bond value guaranteed to the principal.</li>
        <li>
            <strong>Advance Payment/Mobilization Bond:</strong>
        This Bond is given as a guarantee to customers who wish to take monies from their Principal as an advance to execute a project. Vanguard will guarantee refund to the principal all monies paid to the Contractor under this policy.
        </li>
        <li>
            <strong>Retention Bond:</strong>
            Some Companies or organizations allow a contractor to substitute a bond to guarantee the contractor’s faithful execution of repairs to any defects to the completed projects during a specified period. Vanguard will guarantee refund to the principal all monies paid to the Contractor under this policy. If you require this kind of bond for your projects, please contact us.
        </li>
    </ol>
</div>

